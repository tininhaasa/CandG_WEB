                                </li>
                                <li>
                                    4.1.3.3. As varreduras diárias em servidores corporativos podem ser limitadas a pastas ou arquivos específicos, de modo a evitar o comprometimento do desempenho de recursos computacionais críticos;

                                </li>
                                <li>
                                    4.1.3.4. As funções de proteção em tempo real e detecção com base no comportamento devem estar habilitadas para todas as estações de usuários e dispositivos móveis;

                                </li>
                                <li>
                                    4.1.3.5. Sites, serviços e arquivos baixados da internet detectados como possíveis ameaças serão automaticamente bloqueados em estações de usuários, dispositivos móveis e servidores corporativos;
                                </li>
                            </ol>
                            4.1.4. Caso uma estação de usuário ou dispositivo móvel esteja infectado ou com suspeita de infecção de código malicioso, a mesma deverá ser imediatamente isolada da rede corporativa da C&G RPG e de qualquer comunicação com a internet;

                        </li>
                        <li>
                            4.1.5. Caso um servidor corporativo esteja infectado ou com suspeita de infecção de código malicioso, deverão ser adotadas medidas para garantir o isolamento do mesmo da rede corporativa e da internet, levando em consideração o impacto da desativação dos serviços publicados no referido servidor;
                        </li>
                    </ol>

                </li>
                <li>
                    Prevenção dos usuários contra códigos maliciosos
                    <ol>
                        <li>
                            Mesmo com a existência de ferramentas para proteção contra códigos maliciosos, os usuários da C&G RPG devem adotar um comportamento seguro, reduzindo a probabilidade de infecção ou propagação de códigos maliciosos;

                        </li>
                        <li>
                            Os usuários da C&G RPG devem seguir as seguintes regras para proteção contra códigos maliciosos:
                            <ol>
                                <li>
                                    Não tentar efetuar o tratamento e correção de códigos maliciosos por iniciativa própria;

                                </li>
                                <li>
                                    Reportar imediatamente a área de tecnologias da informação qualquer infecção ou suspeita de infecção por código malicioso;

                                </li>
                                <li>
                                    Não desenvolver, testar ou armazenar qualquer parte de um código malicioso de qualquer tipo, a menos que expressamente autorizado;

                                </li>
                                <li>
                                    Efetuar uma varredura com a ferramenta de proteção contra códigos maliciosos fornecida pela C&G RPG antes de utilizar arquivos armazenados em mídias removíveis, baixados da internet ou recebidos nos serviços de e-mail ou comunicadores instantâneos;

                                </li>
                                <li>
                                    Não habilitar MACROS para arquivos recebidos de fontes suspeitas, baixados da internet ou recebidos nos serviços de e-mail ou comunicadores instantâneos. Caso necessário, poderá ser solicitado o apoio da equipe de segurança da informação para validar se o arquivo representa ou não uma ameaça.
                                </li>
                            </ol>
                        </li>
                    </ol>
                </li>
            </ol>
