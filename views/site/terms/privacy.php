<h2 class="center"><strong>POLÍTICA GERAL DE SEGURANÇA DA INFORMAÇÃO</strong></h2>
<ol>
    <li>
        <p><strong>Objetivo:</strong> A política de segurança da informação da C&amp;G RPG tem como objetivo proteger os ativos de informação da empresa, garantir a integridade, confidencialidade e disponibilidade dos dados, e promover a conscientização e boas práticas de segurança entre os colaboradores e parceiros.</p>
    </li>
    <li>
        <p><strong>Responsabilidades:</strong> A alta administração da empresa é responsável por definir e manter esta política. Todos os colaboradores, contratados e parceiros são responsáveis por cumprir as diretrizes aqui estabelecidas.</p>
    </li>
</ol>
<h2><strong>TERMO DE USO DOS SISTEMAS INTERNOS</strong></h2>
<ol>
    <li>
        <p><strong>Acesso Restrito:</strong> Os sistemas internos da C&amp;G RPG são de acesso restrito aos colaboradores autorizados. Os colaboradores devem assinar um termo de responsabilidade ao serem concedidos acessos aos sistemas.</p>
    </li>
    <li>
        <p><strong>Uso Adequado:</strong> Os sistemas internos devem ser usados exclusivamente para fins relacionados ao trabalho. Qualquer uso indevido é estritamente proibido.</p>
    </li>
</ol>
<h2><strong>ACESSO REMOTO</strong></h2>
<ol>
    <li>
        <p><strong>Acesso Seguro:</strong> O acesso remoto aos sistemas da empresa deve ser feito por meio de conexões seguras, como VPNs, e requer autenticação multifator (MFA).</p>
    </li>
    <li>
        <p><strong>Política de Senhas:</strong> Senhas de acesso remoto devem ser fortes, atualizadas periodicamente e nunca compartilhadas.</p>
    </li>
</ol>
<h2><strong>USO DE EQUIPAMENTOS COMPUTACIONAIS PESSOAIS</strong></h2>
<ol>
    <li>
        <p><strong>BYOD (Traga seu próprio dispositivo):</strong> O uso de dispositivos pessoais para acessar sistemas e informações da empresa é permitido apenas após aprovação e configuração de medidas de segurança adequadas.</p>
    </li>
    <li>
        <p><strong>Atualizações de Software:</strong> Todos os dispositivos pessoais devem manter seus sistemas operacionais e aplicativos atualizados com as últimas correções de segurança.</p>
    </li>
</ol>
<h2><strong>NORMA DE CLASSIFICAÇÃO, ROTULAGEM E MANUSEIO DA INFORMAÇÃO</strong></h2>
<ol>
    <li>
        <p><strong>Classificação de Informação:</strong> A informação deve ser classificada em categorias (pública, interna, confidencial) e rotulada de acordo com sua sensibilidade.</p>
    </li>
    <li>
        <p><strong>Manuseio Adequado:</strong> As informações devem ser manuseadas de acordo com sua classificação, com acesso restrito às informações confidenciais.</p>
    </li>
</ol>
<h2><strong>PROTEÇÃO CONTRA CÓDIGOS MALICIOSOS</strong></h2>
<ol>
    <li>
        <p><strong>Antivírus:</strong> Todos os dispositivos da empresa devem possuir software antivírus atualizado e realizar verificações regulares.</p>
    </li>
    <li>
        <p><strong>Conscientização:</strong> Colaboradores devem ser treinados para identificar e relatar ameaças de segurança, como phishing e malware.</p>
    </li>
</ol>
<h2><strong>USO DE SERVIÇOS DE E-MAIL E COMUNICADORES INSTANTÂNEOS</strong></h2>
<ol>
    <li>
        <p><strong>E-mails Seguros:</strong> E-mails contendo informações sensíveis devem ser criptografados. Links e anexos suspeitos devem ser evitados.</p>
    </li>
    <li>
        <p><strong>Uso Responsável:</strong> Comunicadores instantâneos devem ser usados de forma responsável, evitando a divulgação de informações confidenciais.</p>
    </li>
</ol>
<h2><strong>GESTÃO DE IDENTIDADE E CONTROLE DE ACESSO</strong></h2>
<ol>
    <li><strong>Controle de Acesso:</strong> Acesso a sistemas e informações deve ser baseado no princípio do mínimo privilégio, concedendo acesso apenas ao que é necessário para as funções desempenhadas.</li>
</ol>
<h2><strong>ACESSO À INTERNET E COMPORTAMENTO EM MÍDIAS SOCIAIS</strong></h2>
<ol>
    <li><strong>Uso Responsável:</strong> A navegação na internet deve ser para fins relacionados ao trabalho. O uso de mídias sociais deve ser restrito e monitorado.</li>
</ol>
<h2><strong>MONITORAMENTO DE ATIVOS E SERVIÇOS DA INFORMAÇÃO</strong></h2>
<ol>
    <li><strong>Registro de Atividades:</strong> Atividades de sistemas e acesso à informação devem ser registradas e monitoradas regularmente.</li>
</ol>
<h2><strong>RESPOSTA A INCIDENTES DE SEGURANÇA DA INFORMAÇÃO</strong></h2>
<ol>
    <li><strong>Relato de Incidentes:</strong> Todos os incidentes de segurança devem ser imediatamente relatados à equipe de segurança da informação para avaliação e resposta adequada.</li>
</ol>
<h2><strong>NORMA DE USO ACEITÁVEL DE ATIVOS DE INFORMAÇÃO</strong></h2>
<ol>
    <li><strong>Uso Ético:</strong> Os ativos de informação da empresa devem ser usados de maneira ética e legal, respeitando as políticas internas e regulamentos aplicáveis.</li>
</ol>
<p>Esta política de segurança da informação da C&amp;G RPG é dinâmica e está sujeita a revisões periódicas para garantir sua eficácia contínua na proteção dos ativos de informação da empresa. Todos os colaboradores e partes interessadas devem estar cientes e aderir a esta política. O não cumprimento das diretrizes aqui estabelecidas pode resultar em medidas disciplinares ou legais, conforme apropriado.</p>