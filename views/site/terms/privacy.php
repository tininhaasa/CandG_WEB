    <h1 class="text-center my-5"><strong>POLÍTICA GERAL DE SEGURANÇA DA INFORMAÇÃO</strong></h1>

    <h3>ACESSO REMOTO</h3>
    <ol>
        <li>Introdução

            <ol>
                <li>A Norma de segurança da informação N-SI-008 complementa Política Geral de Segurança da Informação, definindo as diretrizes para o acesso remoto a ativos/serviços de informação e recursos computacionais da C&G RPG.</li>
            </ol>
        </li>
        <li>Propósito
            <ol>
                <li>
                    Estabelecer diretrizes para o acesso remoto a ativos/serviços de informação e recursos computacionais da C&G RPG, garantindo níveis adequados de proteção aos mesmos.
                </li>
            </ol>
        </li>
        <li>Escopo
            <ol>
                <li>
                    Esta norma obedece ao escopo definido na Política Geral de Segurança da Informação.
                </li>
            </ol>
        </li>
        <li>Diretrizes

            <ol>
                <li>Concessão e uso do acesso remoto
                    <ol>

                        <li> O acesso remoto a ativos/serviços de informação e recursos computacionais da C&G RPG é restrito a usuários que necessitem deste recurso para execução das atividades profissionais;
                        </li>

                        <li> A realização do acesso remoto, fora do expediente normal de trabalho, não implicará no pagamento de horas extras ao usuário, excetuando-se casos aonde for comprovada a solicitação do trabalho pelo gestor do usuário ou parte autorizada;
                        </li>

                        <li> O usuário será o único responsável por toda ação executada com suas credenciais de acesso remoto, incluindo qualquer atividade não autorizada exercida por terceiros de posse de suas credencias de acesso remoto;
                        </li>

                        <li> O acesso remoto a ativos/serviços de informação e recursos computacionais da C&G RPG será concedido com os privilégios mínimos necessários para execução de suas atividades laborais;
                        </li>

                        <li> Equipamentos computacionais utilizados para acesso remoto devem possuir ferramentas para proteção contra códigos maliciosos aderentes às diretrizes da C&G e firewall local ativo;
                        </li>

                        <li>Em casos de acesso não autorizado, extravio, furto ou roubo de dispositivos computacionais que possam o acesso remoto ao ambiente da C&G habilitado, o usuário responsável deverá informar imediatamente o ocorrido a equipe de segurança da informação.
                        </li>
                    </ol>
                </li>
                <li>Concessão e uso do acesso remoto para terceiros
                    <ol>

                        <li> O acesso remoto a ativos/serviços de informação e recursos computacionais da C&G RPG poderá ser concedido a terceiros ou prestadores de serviço, caso seja necessário para suas atividades laborais;</li>
                        <li>Para concessão e uso do acesso remoto para terceiros, devem ser observadas as seguintes regras:

                            <ol>
                                <li>O acesso remoto de terceiros e prestadores de serviço a ativos/serviços de informação ou recursos computacionais da C&G somente poderá ser concedido após a efetivação do acordo de confidencialidade entre as partes;</li>

                                <li>A concessão do acesso deverá ser limitada automaticamente ao tempo necessário estimado a atividade do terceiro ou prestador de serviço, não excedendo ao máximo de 30 (trinta) dias corridos por concessão;</li>

                                <li>O usuário terceiro, bem como a empresa onde o mesmo trabalha, serão os únicos responsáveis por toda ação executada com suas credenciais de acesso remoto, incluindo qualquer atividade não autorizada exercida por outras partes de posse de suas credencias de acesso remoto;</li>

                                <li>O acesso remoto de terceiros a ativos/serviços de informação e recursos computacionais da C&G RPG será concedido com os privilégios mínimos necessários para execução de suas atividades laborais;</li>

                                <li>Equipamentos computacionais utilizados por terceiros para acesso remoto devem possuir ferramentas para proteção contra códigos maliciosos aderentes às diretrizes da C&G e firewall local ativo;</li>

                                <li>Em casos de acesso não autorizado, extravio, furto ou roubo de dispositivos computacionais de terceiros que possam o acesso remoto ao ambiente da C&G habilitado, o usuário responsável deverá informar imediatamente o ocorrido a equipe de segurança da informação.</li>
                            </ol>
                        </li>
                    </ol>
                </li>
                <li>Monitoramento do acesso remoto

                    <ol>
                        <li>Toda informação que é acessada, transmitida, recebida ou produzida através do acesso remoto a ativos/serviços de informação ou recursos computacionais da C&G está sujeita monitoramento, não havendo por parte do usuário qualquer expectativa de privacidade;</li>


                        <li>Durante o monitoramento do acesso remoto a seus ativos/serviços de informação ou recursos computacionais, a C&G se resguarda o direito de, sem qualquer notificação ou aviso, interceptar, registrar, gravar, ler, copiar e divulgar por, ou para, pessoas autorizadas para finalidades oficiais, incluindo investigações criminais, toda informação trafegada, seja originada de sua rede interna e destinada a redes externas ou o contrário.</li>
                    </ol>
                </li>

            </ol>
        </li>
        <li>Papéis e Responsabilidades

            <ol>
                <li>GERÊNCIA DE SEGURANÇA DA INFORMAÇÃO
                    <ol>
                        <li>É responsabilidade da GERÊNCIA DE SEGURANÇA DA INFORMAÇÃO:
                            <ol>
                                <li>Avaliar, aprovar ou negar solicitações para uso de acesso remoto a ativos/serviços de informação ou recursos computacionais da C&G RPG;</li>

                                <li>Controlar e monitorar qualquer tipo de acesso remoto fornecido pela C&G;</li>

                                <li>Tratar eventuais tentativas de acesso não autorizados ou incidentes de segurança relacionados ao acesso remoto e, quando pertinente, reportar os mesmos ao COMITÊ GESTOR DE SEGURANÇA DA INFORMAÇÃO.</li>
                            </ol>
                        </li>
                    </ol>
                </li>
            </ol>
        </li>
        <li>Sanções e Punições
            <ol>
                <li>Sanções e punições serão aplicadas conforme previsto na Política Geral de Segurança da Informação.</li>
            </ol>
        </li>
        <li>Revisões

            <ol>
                <li>Esta norma é revisada com periodicidade anual ou conforme o entendimento do Comitê Gestor de Segurança da Informação.</li>
            </ol>
        </li>
        <li>8. Gestão da Norma

            <ol>
                <li>A norma N-SI-008 é aprovada pelo Comitê Gestor de Segurança da Informação, em conjunto com a Diretoria da C&G RPG.</li>

                <li>A presente norma foi aprovada no dia 06/12/2023</li>
            </ol>
        </li>

    </ol>
    <h3>USO DE EQUIPAMENTOS COMPUTACIONAIS PESSOAIS</h3>

    <ol>
        <li>Introdução
            <ol>
                <li>A Norma de segurança da informação N-SI-007 complementa Política Geral de Segurança da Informação, definindo as diretrizes para utilização segura de dispositivos computacionais pessoais no ambiente corporativo da C&G RPG ou para o manuseio de informações da C&G RPG.</li>
            </ol>
        </li>
        <li> Propósito
            <ol>
                <li>Estabelecer diretrizes para utilização segura de dispositivos computacionais pessoais no ambiente corporativo da C&G RPG ou para o manuseio de informações da C&G RPG.</li>
            </ol>
        </li>
        <li>Escopo
            <ol>
                <li> Esta norma obedece ao escopo definido na Política Geral de Segurança da Informação.
                </li>
            </ol>

        </li>
        <li>Diretrizes

            <ol>
                <li>Uso de equipamentos computacionais pessoais no ambiente corporativo

                    <ol>
                        <li>
                            A C&G RPG fornece todos os recursos computacionais necessários para que seus colaboradores executem suas atividades laborais;
                        </li>
                        <li>
                            A seu critério exclusivo, a C&G RPG poderá permitir o uso de dispositivos de computação pessoais para execução de trabalho de atividades ou manuseio de informações de sua propriedade;
                        </li>
                        <li>
                            A permissão para o uso de dispositivos de computação pessoais para execução de trabalho de atividades ou manuseio de informações de sua propriedade é uma prerrogativa da diretoria da C&G RPG, devendo o usuário estar formalmente autorizado e concordar integralmente com os termos desta norma, antes de fazer uso de dispositivos pessoais no ambiente corporativo ou para manusear informações de propriedade da C&G RPG;
                        </li>
                        <li>
                            O uso não autorizado de qualquer dispositivo de computação pessoal no ambiente corporativo será considerado uma violação da Politica Geral de Segurança da Informação e tratado como um incidente de segurança da informação, estando o responsável sujeito as sanções e punições previstas neste instrumento;
                        </li>
                        <li>
                            A C&G RPG não será responsável por fornecer suporte, atualização, manutenção, reposição de peças, licenciamento de softwares, reembolso ou cobrir qualquer tipo de custo referente ao uso de dispositivos pessoais;
                        </li>
                        <li>
                            O uso de dispositivos de computação pessoal para atividades de trabalho ou armazenamento de arquivos da C&G RPG não modifica a propriedade da organização sobre as informações criadas, armazenadas, enviadas, recebidas, modificadas ou excluídas. Permanecendo qualquer direito de propriedade intelectual com a C&G RPG;
                        </li>
                        <li>
                            Quando autorizados a praticar o uso de dispositivos de computação pessoais para execução de trabalho de atividades ou manuseio de informações da C&G RPG, usuários serão inteiramente responsáveis por garantir a segurança de seus dispositivos, devendo garantir que:

                            <ol>
                                <li>
                                    O sistema operacional dos dispositivos de computação pessoal estará sempre atualizado e com todas as correções/melhorias de segurança aplicadas;</li>
                                <li>
                                    Dispositivos de computação pessoal possuem ferramenta para prevenção de códigos maliciosos e garantem que as assinaturas de códigos maliciosos são ser atualizadas em tempo real e executam varreduras diariamente;
                                </li>
                                <li>
                                    Dispositivos de computação pessoal utilizam apenas softwares licenciados, preservando o direito autoral.
                                </li>
                            </ol>
                        </li>
                    </ol>
                </li>
            </ol>
        </li>
        <li>Papéis e Responsabilidades

            <ol>
                <li>COMITÊ GESTOR DE SEGURANÇA DA INFORMAÇÃO
                    <ol>
                        <li> É responsabilidade do CGSI:
                            <ol>
                                <li>Avaliar, aprovar ou negar solicitações para uso de dispositivos pessoais no ambiente corporativo.
                                </li>
                            </ol>
                        </li>
                    </ol>
                </li>
            </ol>
        </li>
        <li>Sanções e Punições
            <ol>
                <li>
                    Sanções e punições serão aplicadas conforme previsto na Política Geral de Segurança da Informação.
                </li>
            </ol>
        </li>
        <li>
            Revisões

            <ol>
                <li>
                    Esta norma é revisada com periodicidade anual ou conforme o entendimento do Comitê Gestor de Segurança da Informação.
                </li>
            </ol>
        </li>
        <li>
            Gestão da Norma

            <ol>
