    <h3>NORMA DE CLASSIFICAÇÃO, ROTULAGEM E MANUSEIO DA INFORMAÇÃO</h3>

    <ol>
        <li>
            Introdução
            <ol>
                <li>
                    A Norma de segurança da informação N-SI-001 complementa Política Geral de Segurança da Informação, definindo as diretrizes para a classificação, rotulagem, manuseio, guarda e descarte seguro de informações em formato digital ou em suporte físico.
                </li>
            </ol>
        </li>
        <li>
            Propósito
            <ol>
                <li>
