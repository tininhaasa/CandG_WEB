                                    Os equipamentos da C&G RPG devem ser utilizados com cuidado visando garantir sua preservação e seu funcionamento adequado;
                                </li>
                                <li>
                                    Computadores de mesa (desktops) ou móveis (notebooks) devem ser desligados no final do expediente ou sempre que um usuário estiver ausente por um período prolongado, excetuando-se quando existir uma justificativa plausível em virtude de atividades de trabalho;
                                </li>
                                <li>
                                    A desconexão (log off) da rede deverá ser efetuada nos casos em que o usuário não for mais utilizar o equipamento ou venha a ausentar-se por um período prolongado;
                                </li>
                                <li>
                                    O bloqueio de tela protegido por senha deverá ser ativado sempre que o usuário se afastar do computador de mesa ou móvel que esteja utilizando;
                                </li>
                                <li>
                                    Ao final do contrato de trabalho, os equipamentos disponibilizados para a execução de atividades profissionais devem ser devolvidos em estado de conservação adequado quando no desligamento ou término da relação do usuário com a C&G RPG; e
                                </li>
                                <li>
                                    Qualquer dano aos equipamentos da C&G RPG será devidamente analisado pela área de tecnologia da informação. Havendo a constatação de que tal dano decorreu de ação direta ou omissão do usuário, caberá a C&G RPG exercer seu direito de reparação ao prejuízo, através da tomada das medidas cabíveis.
                                </li>
                            </ol>

                        </li>
                        <li>
                            A seu critério exclusivo a C&G RPG poderá permitir a utilização de equipamento particular para o desempenho de atividades profissionais, devendo os mesmos passar por inspeção tanto do departamento de tecnologia da informação, quanto da área de segurança da informação de forma a garantir adequação aos requisitos e controles de segurança adotados pela empresa;

                        </li>
                        <li>
                            Não é permitida a conexão de equipamentos particulares na rede administrativa da C&G RPG, seja em segmentos cabeados ou sem fio, sem autorização prévia formal e inspeção do equipamento tanto do departamento de tecnologia da informação, quanto da área de segurança da informação.
                        </li>
