    <h3>NORMA DE CLASSIFICAÇÃO, ROTULAGEM E MANUSEIO DA INFORMAÇÃO</h3>

    <ol>
        <li>
            Introdução
            <ol>
                <li>
                    A Norma de segurança da informação N-SI-001 complementa Política Geral de Segurança da Informação, definindo as diretrizes para a classificação, rotulagem, manuseio, guarda e descarte seguro de informações em formato digital ou em suporte físico.
                </li>
            </ol>
        </li>
        <li>
            Propósito
            <ol>
                <li>
                    Estabelecer diretrizes para a classificação, manuseio e rotulagem dos ativos de informação da C&G RPG por seus usuários autorizados.
                </li>
            </ol>
        </li>
        <li>
            Escopo
            <ol>
                <li>
                    Esta norma obedece ao escopo definido na Política Geral de Segurança da Informação.
                </li>
            </ol>
        </li>
        <li>
            Diretrizes
            <ol>
                <li>
                    Classificação e rotulagem da Informação

                    <ol>
                        <li>
                            Para efeitos de classificação da informação, a C&G RPG utiliza as seguintes categorias:

                            <ol>
                                <li>
                                    INFORMAÇÃO PÚBLICA: Informação oficialmente liberada pela C&G RPG para o público geral. A divulgação deste tipo de informação não causa problemas a C&G RPG ou a seus clientes, podendo ser compartilhada livremente com o público geral, desde que seja mantida sua integridade.
                                </li>
                                <li>
                                    INFORMAÇÃO DE USO INTERNO: Informação liberada exclusivamente para usuários e departamentos específicos da C&G RPG, não podendo ser compartilhada com o público em geral. Estas informações só podem ser compartilhadas mediante autorização expressa.
                                </li>
                                <li>
                                    INFORMAÇÃO CONFIDENCIAL: Informação de caráter sigiloso, podendo ser comunicada exclusivamente a usuários especificamente autorizados e que necessitem conhecê-las para o desempenho de suas tarefas profissionais na C&G RPG. A divulgação ou alteração não autorizada desse tipo de informação pode causar graves danos e prejuízos para a C&G RPG e/ou seus clientes, portanto seu compartilhamento deve ser restrito e feito de maneira controlada.
                                </li>
                            </ol>
                        </li>
                        <li>
                            A classificação da informação deverá ser realizada pelos gestores da informação, ou colaboradores designados por estes. Entretanto, a responsabilidade pela assertividade do nível selecionado permanece com o gestor da informação;
                        </li>
                        <li>

                            Para informações classificadas como PÚBLICAS, poderá ser utilizada um rótulo simples, conforme modelos exibidos no Anexo I desta norma;
                        </li>
                        <li>
                            Para informações classificadas como USO INTERNO ou CONFIDENCIAIS, deverá constar no rótulo a sua classificação e, quanto o acesso informação for limitado a um setor/departamento específico, o mesmo deverá ser referenciado, conforme modelos exibidos no Anexo I desta norma;
                        </li>
                        <li>
                            Para a rotulagem da informação, devem ser observados os modelos contidos no Anexo I desta norma.
                        </li>
                    </ol>
                </li>
                <li>
                    Manuseio da Informação

                    <ol>
                        <li>
                            O manuseio da informação da C&G RPG deverá obedecer às regras definidas na Tabela Ação x Classificação, detalhada no Anexo II desta norma;
                        </li>
                        <li>
                            Documentos confidenciais em suporte físico devem ser guardados em gavetas ou armários trancados de forma a impedir o acesso de pessoas não autorizadas;
                        </li>
                        <li>
                            Em períodos de ausência da estação de trabalho, documentos em suporte físico devem ser retirados das mesas e de outras áreas de superfície;
                        </li>
                        <li>
                            Documentos de uso interno ou confidenciais em suporte eletrônico devem ser armazenados em ambientes com acesso controlado e senhas para impedir o acesso a pessoas não autorizadas;
                        </li>
                        <li>
                            Toda não-conformidade será tratada como um incidente de segurança da informação, cabendo uma análise da infração pelo CGSI e aplicação das sanções e punições previstas na Política Geral de Segurança da Informação, conforme a gravidade da violação.
                        </li>
                    </ol>
                </li>
                <li>
                    Descarte da Informação

                    <ol>
                        <li>
                            O descarte da informação deve ser realizado de forma a impedir a recuperação da mesma, independente do seu formato de armazenamento original;
                        </li>
                        <li>
                            O descarte da informação deverá ser realizado conforme os métodos estabelecidos no Anexo III desta norma.
                        </li>
                    </ol>
                </li>
            </ol>

        </li>
        <li>
            Papéis e Responsabilidades

            <ol>
                <li>
                    GESTOR DA INFORMAÇÃO

                    <ol>
                        <li>
                            É responsabilidade dos colaboradores apontados como Gestor da Informação:

                            <ol>
                                <li>
                                    Definir a classificação das informações sob sua responsabilidade com base nas categorias de classificação constantes desta norma, mantendo um registro atualizado dos itens classificados;

                                </li>
                                <li>
                                    Controlar as informações geradas em sua área de negócio e atuação;

                                </li>
                                <li>
                                    Revisar periodicamente a classificação das informações sob sua guarda.
                                </li>

                            </ol>
                        </li>

                    </ol>
                </li>

            </ol>
        </li>
        <li>
            Sanções e Punições

            <ol>
                <li>
                    Sanções e punições serão aplicadas conforme previsto na Política Geral de Segurança da Informação.</li>
        </li>
    </ol>
    </li>
    <li>
        Revisões
        <ol>
            <li>
                Esta norma é revisada com periodicidade anual ou conforme o entendimento do Comitê Gestor de Segurança da Informação.
            </li>

        </ol>
    </li>
    <li>
        Gestão da Norma
        <ol>
            <li>

                A norma N-SI-001 é aprovada pelo Comitê Gestor de Segurança da Informação, em conjunto com a Diretoria da C&G RPG.
            </li>
            <li>
                A presente norma foi aprovada no dia 06/12/2023</li>
        </ol>
    </li>


    </ol>
    <h3> NORMA DE USO ACEITÁVEL DE ATIVOS DE INFORMAÇÃO</h3>

    <ol>
        <li>
            Introdução

            <ol>
                <li>
                    A Norma de segurança da informação N-SI-002 complementa Política Geral de Segurança da Informação, definindo as diretrizes para o uso aceitável de ativos de informação da C&G RPG por seus usuários autorizados.
                </li>
            </ol>
        </li>
        <li>
            Propósito

            <ol>
                <li>
                    Estabelecer diretrizes para o uso aceitável, entendido como seguro, dos ativos de informação da C&G RPG por seus usuários autorizados.
                </li>
            </ol>
        </li>
        <li>
            Escopo

            <ol>
                <li>
                    Esta norma obedece ao escopo definido na Política Geral de Segurança da Informação.
                </li>
            </ol>
        </li>
        <li>
            Diretrizes

            <ol>
                <li>Uso de equipamento computacional

                    <ol>
                        <li>
                            A C&G RPG fornece para seus usuários equipamentos para o desempenho exclusivamente de suas atividades profissionais.

                        </li>
                        <li>Todo usuário deve observar as seguintes disposições quanto ao uso de equipamentos de propriedade da C&G RPG:
                            <ol>
                                <li>
                                    Os equipamentos disponibilizados com o objetivo específico de permitir aos usuários desenvolverem suas atividades profissionais são de propriedade da C&G RPG, sendo expressamente proibida a utilização para fins particulares;
                                </li>
                                <li>
                                    A alteração e/ou a manutenção de qualquer equipamento de propriedade da C&G RPG é uma atribuição específica do departamento de tecnologia da informação que, a seu critério exclusivo, poderá delegar formalmente outro responsável. Demais usuários são expressamente proibidos de realizar qualquer tipo de manutenção ou modificação nos equipamentos;
                                </li>
                                <li>
                                    Os equipamentos da C&G RPG devem ser utilizados com cuidado visando garantir sua preservação e seu funcionamento adequado;
                                </li>
                                <li>
                                    Computadores de mesa (desktops) ou móveis (notebooks) devem ser desligados no final do expediente ou sempre que um usuário estiver ausente por um período prolongado, excetuando-se quando existir uma justificativa plausível em virtude de atividades de trabalho;
                                </li>
                                <li>
                                    A desconexão (log off) da rede deverá ser efetuada nos casos em que o usuário não for mais utilizar o equipamento ou venha a ausentar-se por um período prolongado;
                                </li>
                                <li>
                                    O bloqueio de tela protegido por senha deverá ser ativado sempre que o usuário se afastar do computador de mesa ou móvel que esteja utilizando;
                                </li>
                                <li>
                                    Ao final do contrato de trabalho, os equipamentos disponibilizados para a execução de atividades profissionais devem ser devolvidos em estado de conservação adequado quando no desligamento ou término da relação do usuário com a C&G RPG; e
                                </li>
                                <li>
                                    Qualquer dano aos equipamentos da C&G RPG será devidamente analisado pela área de tecnologia da informação. Havendo a constatação de que tal dano decorreu de ação direta ou omissão do usuário, caberá a C&G RPG exercer seu direito de reparação ao prejuízo, através da tomada das medidas cabíveis.
                                </li>
                            </ol>

                        </li>
                        <li>
                            A seu critério exclusivo a C&G RPG poderá permitir a utilização de equipamento particular para o desempenho de atividades profissionais, devendo os mesmos passar por inspeção tanto do departamento de tecnologia da informação, quanto da área de segurança da informação de forma a garantir adequação aos requisitos e controles de segurança adotados pela empresa;

                        </li>
                        <li>
                            Não é permitida a conexão de equipamentos particulares na rede administrativa da C&G RPG, seja em segmentos cabeados ou sem fio, sem autorização prévia formal e inspeção do equipamento tanto do departamento de tecnologia da informação, quanto da área de segurança da informação.
                        </li>
                    </ol>
                </li>

                <li>
                    Dispositivos de Armazenamento Removível
                    <ol>
                        <li>
                            A C&G RPG poderá, a seu critério exclusivo, fornecer a seus usuários dispositivos móveis ou com capacidade de armazenamento removível para execução de atividades profissionais, devendo ser observadas além das diretrizes acima, as seguintes:
                            <ol>
                                <li>
                                    O usuário é o responsável direto pela segurança física e lógica dos dispositivos móveis sob sua guarda. Portanto, os mesmos não devem ficar fora de seu alcance em locais públicos onde haja acesso não controlado de pessoas;

                                </li>
                                <li>
                                    Durante o deslocamento o usuário deverá estar alerta e ter uma conduta discreta, dando preferência para compartimentos de armazenamento resistentes e não chamativos e nunca deixando o dispositivo móvel desacompanhado em veículos;

                                </li>
                                <li>
                                    A instalação de ferramentas de proteção para dispositivos móveis é realizada pelo departamento de tecnologia da informação e é obrigatória para todos os equipamentos corporativos; e

                                </li>
                                <li>
                                    Em caso de perda ou furto de um dispositivo de armazenamento removível, o usuário deve comunicar imediatamente o departamento de segurança patrimonial para que possam ser tomadas as medidas cabíveis.
                                </li>
                            </ol>
                        </li>
                    </ol>
                </li>
                <li>
                    Armazenamento remoto (nuvem)
                    <ol>
                        <li>
                            A C&G RPG disponibiliza para seus usuários espaço para armazenamento remoto de arquivos na nuvem, através de sua solução corporativa;

                        </li>
                        <li>
                            Não é permitido o uso de qualquer outra solução de armazenamento na nuvem, que não seja a oficialmente adotada pela empresa e homologada pela equipe de segurança da informação da C&G RPG.
                        </li>
                    </ol>
                </li>
                <li>
                    Identificação digital
                    <ol>
                        <li>
                            A C&G RPG poderá, a seu critério exclusivo, fornecer certificados digitais para usuários que execução de atividades profissionais específicas, devendo ser observadas as seguintes diretrizes:
                            <ol>
                                <li>
                                    Cabe exclusivamente ao usuário a conservação de seu certificado digital, independentemente do equipamento que o suporte, bem como de qualquer tipo de senha ou meio de autenticação relacionado ao mesmo.

                                </li>
                                <li>
                                    O usuário deverá informar a equipe de segurança da informação sobre qualquer eventos ou suspeitas relativas ao comprometimento de sua senha e/ou o uso indevido de seu certificado digital;

                                </li>
                                <li>
                                    O usuário desligado ou em processo de desligamento terá o certificado digital expedido pela C&G RPG imediatamente revogado;

                                </li>
                                <li>
                                    É de responsabilidade da área de segurança da informação prover a atualização de todos os pontos de verificação com as respectivas listas de revogação.
                                </li>
                            </ol>
                        </li>
                    </ol>
                </li>
                <li>
                    Equipamentos de impressão e reprografia
                    <ol>
                        <li>
                            O uso de equipamentos de impressão e reprografia (fotocopiadoras) deve ser feito exclusivamente para a impressão/reprodução de documentos que sejam de interesse da C&G RPG ou que estejam relacionados com o desempenho das atividades profissionais do usuário.

                        </li>
                        <li>
                            O usuário deve observar as seguintes disposições específicas quanto ao uso de equipamentos de impressão e reprografia:
                            <ol>
                                <li>
                                    O usuário deve retirar imediatamente da impressora ou fotocopiadora os documentos que tenha solicitado a impressão, transmissão ou cópia que contenham informações da C&G RPG, classificadas como de uso interno ou confidencial;

                                </li>
                                <li>
                                    A impressão ou cópia de documento em suporte físico deve ser limitada à quantidade exata necessária para a tarefa determinada;

                                </li>
                                <li>
                                    Não será admissível, em nenhuma hipótese, o reaproveitamento de páginas já impressas e contendo informações classificadas como confidenciais, devendo as mesmas ser descartadas de acordo com os procedimentos adotados pela C&G RPG.
                                </li>
                            </ol>
                        </li>
                    </ol>
                </li>
                <li>
                    Segurança física
                    <ol>
                        <li>
                            As instalações de processamento das informações da C&G RPG serão mantidas em áreas seguras, cujo perímetro é fisicamente isolado contra o acesso não autorizado, os danos e quaisquer interferências de origem humana ou natural.
                        </li>
                        <li>
                            O usuário deve observar as seguintes disposições específicas quanto à segurança física:
                            <ol>
                                <li>
                                    Crachás de identificação, inclusive temporários, são pessoais e intransferíveis. Sob nenhuma circunstância será permitido o seu compartilhamento;

                                </li>
                                <li>
                                    Enquanto em áreas sensíveis, os colaboradores devem portar crachás de identificação que exibam claramente seu nome e fotografia. Terceiros autorizados devem portar crachás temporários identificando claramente que os mesmos não são colaboradores da C&G RPG;

                                </li>
                                <li>
                                    Excetuando-se quando formalmente autorizado, terceiros nunca devem ser deixados sozinhos em áreas sensíveis;

                                </li>
                                <li>
                                    É proibida qualquer tentativa de se obter ou permitir o acesso a indivíduos não autorizado a áreas sensíveis da C&G RPG;

                                </li>
                                <li>
                                    É resguardado a C&G RPG o direito de inspecionar malas, maletas, mochilas e similares, assim como quaisquer equipamentos, incluindo dispositivos móveis, antes de permitir a entrada ou saída de colaboradores ou terceiros de áreas sensíveis;

                                </li>
                                <li>
                                    É resguardado a C&G RPG o direito de monitorar seus ambientes físicos. Para isso será utilizado sistema de circuito fechado de televisão em áreas comuns. As imagens obtidas serão armazenadas e protegidas contra qualquer tipo de manipulação indevida;

                                </li>
                                <li>
                                    Os documentos classificados como internos ou confidenciais, após manuseados, não deverão ser deixados expostos em cima de mesas, assim, ao se ausentar cabe usuário o dever de mantê-los guardados ou descartá-los de acordo com os procedimentos determinados organização;

                                </li>
                                <li>
                                    4.6.2.8. Não é permitido consumir qualquer tipo de alimento, bebida ou fumar em áreas apontadas como sensíveis.</li>
                            </ol>
                        </li>
                    </ol>
                </li>
            </ol>
        </li>
