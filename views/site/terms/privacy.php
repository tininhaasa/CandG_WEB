            <ol>
                <li>Uso de equipamento computacional

                    <ol>
                        <li>
                            A C&G RPG fornece para seus usuários equipamentos para o desempenho exclusivamente de suas atividades profissionais.

                        </li>
                        <li>Todo usuário deve observar as seguintes disposições quanto ao uso de equipamentos de propriedade da C&G RPG:
                            <ol>
                                <li>
                                    Os equipamentos disponibilizados com o objetivo específico de permitir aos usuários desenvolverem suas atividades profissionais são de propriedade da C&G RPG, sendo expressamente proibida a utilização para fins particulares;
                                </li>
